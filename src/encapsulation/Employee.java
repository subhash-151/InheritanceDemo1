package encapsulation;



public class Employee {
	
	private String name;
	private int salery;
	private int id;
	
	public String getName() {
		
		return name;
	}
	
	public void setName(String name) {
		
		this.name=name;
		
		
	}
	
	public int getsalery() {
		
		return salery;
		}
	public void setsalery(int salery) {
		
		this.salery=salery;
	}
	
	public int getid() {
		
		return id;
		}
	
	public void setid(int id) {
		
		this.id=id;
		
		
	}
	

}
